a = 5
b = 2

# 기초 산술 연산자
print("a + b: ", a + b) # 7 
print("a - b: ", a - b) # 3
print("a * b: ", a * b) # 10
print("a / b: ", a / b) # 2.5
print("a // b: ", a // b)   # 2 몫 
print("a % b: ", a % b) # 1 나머지 
print("a ** b: ", a ** b)   # 25 거듭 제곱(5의 2승)

